AYDINLATMA METNİ

<br>Turkuvaz Müzik Kitap Mağazacılık Pazarlama A.Ş. (“Turkuvaz”) “veri sorumlusu” olarak, 6698 sayılı Kişisel Verilerin Korunması Kanunu (“KVKK”) ve ilgili mevzuat uyarıncakişisel verilerinizi işlemekte, işleme faaliyetlerine yönelik kanunlarda öngörülmüş tüm teknik ve idari tedbirleri alarak gerekli korumaları sağlamaktayız.

<br>Bu kapsamda, Turkuvaz, D&R Kart üyeliğiniz kapsamında işlenecek olan kişisel verilerinizi, KVKK ve ilgili mevzuatın çizdiği sınırlar çerçevesinde, KVKK’nın 4. maddesinde yer alan;

<br> Hukuka ve dürüstlük kuralına uygun olma,

<br> Doğru ve gerektiğinde güncel olma,

<br>Belirli, açık ve meşru amaçlar için işlenme,

<br>İşleme amaçlarıyla bağlantılı, sınırlı ve ölçülü olma,

<br>İlgili mevzuatta öngörülen veya işlendikleri amaç için gerekli olan süre kadar muhafaza edilme

ilkelerine uygun olarak işlemektedir.
<br>
1. KİŞİSEL VERİLERİNİZİN İŞLENME AMAÇLARI VE HUKUKİ SEBEPLERİ
<br>
D&R Kart üyeliğiniz kapsamında işlenecek olan;

<br> Kart numaranız; D&R Kart üyeliğinizin gerçekleştirilebilmesi, D&R Kart üyeliği sisteminde ayırt edilebilirliğinizin sağlanabilmesi, D&R Kart güvenliğinizin sağlanabilmesi,

<br>  Ad ve soyadınız; D&R Kart üyeliğinizin gerçekleştirilebilmesi, D&R Kart üyeliği sözleşmenizin kurulabilmesi, D&R Kart üyeliği sisteminde ayırt edilebilirliğinizin sağlanabilmesi, D&R Kart ile yapacağınız alışverişlerde fatura isminizin belirlenebilmesi,

<br> Cep telefonu numaranız; D&R Kart üyeliğinizin gerçekleştirilebilmesi, D&R Kart sisteminde ayırt edilebilirliğinizin sağlanabilmesi, D&R Kartınızın cep telefonu numaranıza tanımlanabilmesi, D&R Kart üyelik sistemine aynı cep telefon numarası ile birden fazla kayıt yapılabilmesinin önüne geçilebilmesi, D&R Kart üyeliğiniz ile ilgili konularda sizinle iletişim kurulmasının gerekmesi halinde size ulaşılabilmesi, tarafımızca sunduğumuz hizmet ve ürünlerle ilgili size bilgilendirmenin sağlanabilmesi, şikayet ve memnuniyetsizlik durumlarında sizlere ulaşılabilmesi,

<br> E-posta adresiniz; D&R Kart üyeliğiniz ile ilgili konularda sizinle iletişim kurulmasının gerekmesi halinde size ulaşılabilmesi, tarafımızca sunduğumuz hizmet ve ürünlerle ilgili size bilgilendirmenin sağlanabilmesi, şikâyet ve memnuniyetsizlik durumlarında sizlere ulaşılabilmesi,

<br> Hukuki onaylarınız; onay kayıtlarınızı hukuka uygun şekilde saklanabilmesi,

<br> D&R Kartınızı kullanarak aldığınız ürünlerin adları, kategorileri, detay bilgileri, adet ve tutar miktarları; D&R Kartınızı tüm özelliklerinden yararlanarak kullanabilmenizin sağlanabilmesi, kazandığınız D&R Kart puanlarının hesaplanabilmesi, ürünlere yönelik kampanyalar düzenlenebilmesi ve bu kampanyalardan faydalandırılabilmeniz,

<br>  D&R Kartınızı kullanarak yaptığınız alışverişlerde kullandığınız ödeme yöntemlerinin türleri ve yararlandığınız kampanya bilgileri; D&R Kart puanlarınızın hesaplanabilmesi, ödeme yöntemlerinizin türlerine göre kampanyaların düzenlenebilmesi ve bu kampanyalardan faydalandırılabilmeniz,

<br> D&R Kartınızı kullanarak yaptığınız alışverişlerin yapıldığı kanal, tarih, saat ve mağaza bilgileri; D&R Kart puanlarınızın ömrünün hesaplanabilmesi, yaptığınız alışverişlerin D&R Kart kullanım alanı dahilindeki mağazalarımızda yapıldığının teyit edilebilmesi, D&R Kart sisteminden öngörülenden fazla yararlanılmasının ve haksız kazanç sağlanmasının önüne geçilebilmesi, kanallara yönelik kampanyalar düzenlenebilmesi ve bu kampanyalardan faydalandırılabilmeniz

amaçları ile, D&R Kart üyeliği sözleşmesinin kurulması veya ifasıyla doğrudan doğruya ilgili olması kaydıyla, kişisel verilerinizin işlenmesinin gerekli olması, temel hak ve özgürlüklerinize zarar vermemek kaydıyla, Turkuvaz’ın meşru menfaatleri için kişisel verilerinizi işlemenin zorunlu olması ve bir hakkın tesisi, kullanılması veya korunması için veri işlemenin zorunlu olması hukuki sebeplerinden her biri dahilinde işlenecektir.

Ayrıca, elektronik ticari ileti gönderilmesine onay ve bu kapsamda/ amaçla kişisel verilerinizin işlenmesine buaçık rıza vermeniz halinde, D&R Kart üyeliğiniz kapsamında işlenecek olan ad ve soyadınız, cep telefonu numaranız, e-posta adresiniz ve ses kaydınız;sunduğumuz hizmet ve ürünlerle ilgili tarafınıza reklam, tanıtım, pazarlama ve kutlama/ temenni amaçlı her türlü ticari elektronik iletilerin gönderilmesi amacıyla açık rıza hukuki sebebi dahilinde işlenecektir.

<br>2. KİŞİSEL VERİLERİNİZİN TOPLANMA YÖNTEMİ

<br>D&R Kart üyeliğiniz kapsamında işlenecek olan kişisel verileriniz,

<br>  D&R Kart sistemine kaydolurken paylaştığınız bilgiler,

<br>  D&R Kartınızı kullanarak yaptığınız işlemler aracılığı ile

tamamen veya kısmen otomatik olan ya da bir veri kayıt sisteminin parçası olmak kaydıyla otomatik olmayan yollarla toplanmaktadır.

<br>3. KİŞİSEL VERİLERİNİZİN AKTARILMASI

<br>D&R Kart üyeliğiniz kapsamında işlenecek olan kişisel verileriniz;

<br>  Avukatlar, denetçiler, vergi danışmanları ile danışmanlık ve hizmet aldığımız diğer üçüncü kişilere; iş süreçlerimizi hukuka ve meşru menfaatlerimize uygun olarak yürütebilmemiz, olası bir yargı sürecinde kanuni hakkımızın kullanılması,

<br>Tarafınızca yetki verilmiş olan vekil ve temsilcilerinize; hukuki yükümlülüklerimizin yerine getirilebilmesi,

<br> Düzenleyici ve denetleyici kurumlar ile mahkeme ve icra müdürlükleri gibi sair resmî kurumlara, kişisel verilerinizi talep etmeye yetkili olan diğer kamu kurum veya kuruluşlarına, hukuki yükümlülüklerimizin yerine getirilebilmesi,

<br>Turkuvaz Grubu şirketlerine, iş ve program ortaklarımıza, ifa yardımcılarımıza, tedarikçilerimize, yukarıda yer alan işleme amaçlarının gerçekleştirilebilmesi, iş süreçlerinde ve ürün ve hizmetlerin sunulmasında/ geliştirilmesinde destek alınabilmesi, saklama ve arşivleme faaliyetlerinin yürütülmesi ve altyapı ve teknik güvenliğinin sağlanabilmesi

amaçlarıyla KVKK’nın 8. maddesi uyarınca aktarılabilecektir.

<br>4. KİŞİSEL VERİLERİNİZİN KORUNMASINA YÖNELİK HAKLARINIZ

<br>D&R Kart üyeliğiniz kapsamında işlenecek olan kişisel verileriniz ile ilgili olarak;

<br>a.     Kişisel veri işlenip işlenmediğini öğrenme,

<br>b.     Kişisel verileri işlenmişse buna ilişkin bilgi talep etme,

<br>c.     Kişisel verilerin işlenme amacını ve bunların amacına uygun kullanılıp kullanılmadığını öğrenme,

<br>ç.     Yurt içinde veya yurt dışında kişisel verilerin aktarıldığı üçüncü kişileri bilme,

<br>d.     Kişisel verilerin eksik veya yanlış işlenmiş olması hâlinde bunların düzeltilmesini isteme,

<br>e.     KVKK’nın 7. maddede öngörülen şartlar çerçevesinde kişisel verilerin silinmesini veya yok edilmesini isteme,

<br>f.      (d) ve (e) bentleri uyarınca yapılan işlemlerin, kişisel verilerin aktarıldığı üçüncü kişilere bildirilmesini isteme,

<br>g.     İşlenen verilerin münhasıran otomatik sistemler vasıtasıyla analiz edilmesi suretiyle kişinin kendisi aleyhine bir sonucun ortaya çıkmasına itiraz etme,

<br>ğ.     Kişisel verilerin kanuna aykırı olarak işlenmesi sebebiyle zarara uğraması hâlinde zararın giderilmesini talep etme

haklarına sahipsiniz.

<br>Belirtilen haklarınız kapsamında taleplerinizi, iadeli taahhütlü mektup aracılığıyla veya şahsen veya noter aracılığıyla yazılı olarak veya kayıtlı elektronik posta (KEP) adresi, güvenli elektronik imza, mobil imza ya da daha önce bize bildirdiğiniz ve sistemimizde kayıtlı bulunan elektronik posta adresiniz var ise elektronik posta adresinizi kullanmak suretiyle bize ulaşarak iletebilirsiniz.

<br>Başvurunuz, belirtilen yollar aracılığıyla bize ulaştırılmasından itibaren, başvurunuzun niteliğine göre en geç 30 (otuz) gün içinde ücretsiz olarak sonuçlandırılacak olup, başvuranın doğru kişi olduğunun teyit edilmesi amacıyla Turkuvaz sizden bazı doğrulayıcı bilgiler talep edebilecektir; bu bilgiler yalnızca gerçek veri sahibinin kim olduğunun tespit edilmesi ve başvuru sonuçlarının doğru kişiyle paylaşılması adına talep edilecektir.

<br>Kişisel Verileri Koruma Kurumu tarafından 10.03.2018 tarihli ve 30356 sayılı Resmî Gazete'de yayımlanan Veri Sorumlusuna Başvuru Usul ve Esasları Hakkında Tebliğ’de belirtildiği üzere, başvurunuza cevabın yazılı olarak verilmesi halinde ilk on sayfa üzerindeki her sayfa için 1 TL ve cevabın CD, harici bellek gibi bir kayıt ortamında verilmesi halinde kayıt ortamının maliyetini geçmeyecek bir ücret tarafınızdan talep edilebilecektir. Başvurunuzun Turkuvaz’dan kaynaklanması halinde var ise alınan ücret iade edilecektir.

<br>Turkuvaz, işbu D&R Kart Üyeliği Aydınlatma Metni’ni, KVKK ve ilgili mevzuat hükümlerindeki değişiklikler çerçevesinde her zaman güncelleme hakkını saklı tutar.

